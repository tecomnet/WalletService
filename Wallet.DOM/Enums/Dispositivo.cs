namespace Wallet.DOM.Enums;

/// <summary>
/// Representa el tipo de dispositivo desde el cual se accede a la aplicación.
/// </summary>
public enum Dispositivo
{
    /// <summary>
    /// Acceso desde una aplicación móvil o de escritorio.
    /// </summary>
    App = 0,
    /// <summary>
    /// Acceso desde un navegador web.
    /// </summary>
    Web = 1,
}