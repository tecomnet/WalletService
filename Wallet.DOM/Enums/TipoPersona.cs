namespace Wallet.DOM.Enums
{
    /// <summary>
    /// Define los tipos de persona que pueden ser registrados en el sistema.
    /// </summary>
    public enum TipoPersona
    {
        /// <summary>
        /// Representa a una persona física (individuo).
        /// </summary>
        Fisica = 0,
        /// <summary>
        /// Representa a una persona moral (empresa o entidad legal).
        /// </summary>
        Moral = 1,
        /// <summary>
        /// Representa a una persona extranjera.
        /// </summary>
        Extranjero = 2,
    }
}
